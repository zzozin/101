package class101web;

public class Product {
	private String name;
	private int price;
	
	//sourse의 Generate Getters and Setters값
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getPrice() {
		return price;
	}
	public void setPrice(int price) {
		this.price = price;
	}
	

}
